async function seedFunny(type: 'win' | 'near' | 'miss', items: string[]) {
    for (const text of items) {
        const exists = await db
            .select({ id: funnyMessages.id })
            .from(funnyMessages)
            .where(and(eq(funnyMessages.type, type), eq(funnyMessages.text, text)) as any);
        if (exists.length === 0) {
            await db.insert(funnyMessages).values({ type, text });
        }
    }
}

async function main() {
    await seedFunny('win', winMessages);
    await seedFunny('near', nearMessages);
    await seedFunny('miss', missMessages);
    console.log('Seeded funny messages (win, near, miss)');
}

main().then(() => process.exit(0)).catch((e) => { console.error(e); process.exit(1); });


