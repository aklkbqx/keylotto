async function seedFunny(type: 'win' | 'near' | 'miss', items: string[]) {
    for (const text of items) {
        const exists = await db
            .select({ id: funnyMessages.id })
            .from(funnyMessages)
            .where(and(eq(funnyMessages.type, type), eq(funnyMessages.text, text)) as any);
        if (exists.length === 0) {
            await db.insert(funnyMessages).values({ type, text });
        }
    }
}

